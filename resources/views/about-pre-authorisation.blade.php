    <div class="container mx-auto bg-white p-5 space-y-10">
        <div class="space-y-3">
            <h2 class="font-bold text-xl">What Are They?</h2>
            <p>
                Pre-authorisations are simply holds placed on funds in your bank account. It ensures that you have
                sufficient funds in your account to cover the payment. The money never leaves your card, it is
                simply held by your card provider and usually deducted from your "available balance" until it is either
                collected, cancelled or expires after 7 days.
            </p>
        </div>
        <div class="space-y-3">
            <h2 class="font-bold text-xl">Is it a deposit?</h2>
            <p>
                No. Pre-authorisations through myTables are simply held until the time of your booking. When you arrive
                for your booking, the restaurant will then check you in; causing the funds to automatically be released
                back to you. The funds are then available to you straight away as they were held by your card provider.
            </p>
        </div>
        <div class="space-y-3">
            <h2 class="font-bold text-xl">Do all bookings require pre-authorisation?</h2>
            <p>
                No. The choice to take pre-authorisations for booking is up to the restaurant you book with as is the
                amount they choose to hold. It is also at the discretion of the restaurant as to whether the
                pre-authorised funds are collected in the event of a "no-show" (failing to turn up for a booking).
            </p>
        </div>
        <div class="space-y-3">
            <h2 class="font-bold text-xl">How long are the funds held?</h2>
            <p>
                If your booking takes place within the next 24 hours, the funds will be held in your bank account
                immediately upon booking. If your booking takes place outwith the next 24 hours, the funds will be held
                24 hours before your booking. The funds can be released by the restaurant as soon as you arrive for your
                booking. If you fail to show up for your booking, the restaurant may choose to collect the funds,
                at which point your card will be charged. If the restaurant fail to check you in or collect the funds,
                they will be automatically released by your bank 7 days after the pre-authorisation was initiated.
            </p>
        </div>
    </div>

